    private void List() throws Exception{
        if(coursebll.getListOnline()==null)
            coursebll.listOnline();
        ArrayList<OnlineCourseDTO> list = coursebll.getListOnline();
        Vector header=new Vector();
        header.add("Mã khoá học");
        header.add("Tên khoá họv");
        header.add("Số tín chỉ");
        header.add("Khoa");
        header.add("URL");
        if (modelOnline.getRowCount() == 0) 
                modelOnline = new DefaultTableModel(header, 0);
        for(OnlineCourseDTO p: list) {
            Vector row=new Vector();
            row.add(p.getCourseID());
            row.add(p.getTitle());
            row.add(p.getCredits());
            row.add(p.getDepartmentID());
            row.add(p.getUrl());
            modelOnline.addRow(row);
        }
        tbOnline.setModel(modelOnline);
    }
    
